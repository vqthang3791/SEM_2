package Java1.Lab1;

public class TestconditionalOperators {
    public static void main(String[] args) {
        int value1 = 10, value2 = 20;

        System.out.println("value1 == value2:");
        System.out.println(value1 == value2);

        System.out.println("value1 != value2:");
        System.out.println(value1 != value2);

        System.out.println("value1 > value2:");
        System.out.println(value1 > value2);

        System.out.println("value1 < value2:");
        System.out.println(value1 < value2);

        System.out.println("value1 <= value2");
        System.out.println(value1 <= value2);
    }
}
