package Java1.Lab1;

public class testbetwiseOperators {

    public static void main(String[] args) {
        int x = 23, y = 12;

        System.out.print("x & y:");
        System.out.println(x & y);

        System.out.print("x|y:");
        System.out.println(x | y);

        System.out.print("x^y:");
        System.out.println(x ^ y);

        int a = 43, b = 1;

        System.out.print("a >> b:");
        System.out.println(a >> b);

        System.out.print("a << b:");
        System.out.println(a << b);
    }
}
